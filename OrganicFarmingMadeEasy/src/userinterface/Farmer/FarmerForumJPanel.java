    private void btnAddQuestionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddQuestionActionPerformed
        // TODO add your handling code here:
        
        if(txtQuestion.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Question field cannot be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
        Forum forum = this.organization.getForum();
        System.out.println("Forum "+forum);
        Question question = new Question();
        question.setQuestion(txtQuestion.getText());
        question.setFarmerAccount(userAccount);
        forum.getQuestionList().add(question);
        
        populateForumTable();
        }
    }//GEN-LAST:event_btnAddQuestionActionPerformed

    private void btnAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnswerActionPerformed
        // TODO add your handling code here:
        
        
        
        int selectedRow = tblForum.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a question", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Question question = (Question) tblForum.getValueAt(selectedRow, 0);
            
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                userProcessContainer.add("FarmerForumAnswerJPanel", new FarmerForumAnswerJPanel(userProcessContainer,userAccount,enterprise,organization,question));
                layout.next(userProcessContainer);
            
            
        }
        
    }//GEN-LAST:event_btnAnswerActionPerformed

