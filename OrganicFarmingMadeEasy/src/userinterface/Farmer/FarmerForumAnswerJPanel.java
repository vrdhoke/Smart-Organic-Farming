    public void populateAnswerTable(){
        DefaultTableModel model = (DefaultTableModel) tblAnswer.getModel();
        if(question.getAnswersList().size()>0){
            model.setRowCount(0);
            for (Answer answer : question.getAnswersList()){
                Object[] row = new Object[3];
                row[0] = answer;
                row[1] = answer.getText();
                row[2] = answer.isIsRelevant()? "Usefull": "";
                model.addRow(row);
        }
        }
        else JOptionPane.showMessageDialog(null, "No one has answered this question");
               
    }
    
    
