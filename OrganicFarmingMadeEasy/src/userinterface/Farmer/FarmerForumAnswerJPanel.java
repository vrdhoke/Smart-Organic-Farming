    private void btnSubmitAnswerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitAnswerActionPerformed
        // TODO add your handling code here:
        
        if(txtAnswer.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Answer field cannot be empty!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
        Answer answer = new Answer();
        answer.setText(txtAnswer.getText());
        answer.setFarmerAccount(userAccount);
        question.getAnswersList().add(answer);
        
        populateAnswerTable();
        }
    }//GEN-LAST:event_btnSubmitAnswerActionPerformed

