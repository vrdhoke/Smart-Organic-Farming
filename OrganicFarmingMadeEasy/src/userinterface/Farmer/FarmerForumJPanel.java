        public void populateForumTable(){
        DefaultTableModel model = (DefaultTableModel) tblForum.getModel();
        
        model.setRowCount(0);
        for (Question question : organization.getForum().getQuestionList()){
            Object[] row = new Object[2];
            row[0] = question;
            row[1] = question.getQuestion();
       
            model.addRow(row);
        }
    }
