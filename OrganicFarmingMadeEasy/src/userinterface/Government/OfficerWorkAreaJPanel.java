    public void populateTable(){
        DefaultTableModel model = (DefaultTableModel)tblFarmerSubsidyRequest.getModel();
        
        
        
        model.setRowCount(0);
//        List<WorkRequest> temp = new ArrayList<>();
        Organization org = null;
            for(Network network : business.getNetworkList()){
                for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                    if(enterprise instanceof GovernmentEnterprise){
                        for(Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
                            if(organization instanceof OfficerOrganization){
                            org = organization;
                            break;
                            }
                        }

                    }
                }
            }
        for(WorkRequest request : org.getWorkQueue().getWorkRequestList()){
//            temp.add(request);
            Object[] row = new Object[4];
            row[0] = request;
            row[1] = request.getSender().getEmployee().getName();
            row[2] = request.getReceiver() == null ? null : request.getReceiver().getEmployee().getName();
            row[3] = request.getStatus();
            
            model.addRow(row);
        }
    }
    
