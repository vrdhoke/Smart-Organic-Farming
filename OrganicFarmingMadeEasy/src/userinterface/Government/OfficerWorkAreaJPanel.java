    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed

        int selectedRow = tblFarmerSubsidyRequest.getSelectedRow();
        
        int rowCount = tblFarmerSubsidyRequest.getRowCount();
        
        if(rowCount==0){
            JOptionPane.showMessageDialog(null, "There is nothing to Assign");
            return;
        }

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row !");
            return;
        }

        request = (WorkRequest)tblFarmerSubsidyRequest.getValueAt(selectedRow, 0);
        if(request.getStatus().equalsIgnoreCase("Subsidy Provided")|| request.getStatus().equalsIgnoreCase("Subsidy Rejected")){
            JOptionPane.showMessageDialog(null, "Subsidy request already assigned");
        }
        else{
        request.setReceiver(userAccount);
        request.setStatus("Pending");
        populateTable();
        }
    }//GEN-LAST:event_btnAssignToMeActionPerformed

    private void btnViewRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewRequestActionPerformed
        
            
            int selectedRow = tblFarmerSubsidyRequest.getSelectedRow();
            
            int rowCount = tblFarmerSubsidyRequest.getRowCount();
        
            if(rowCount==0){
            JOptionPane.showMessageDialog(null, "There is nothing to View");
                return;
            }

            if (selectedRow < 0){
                JOptionPane.showMessageDialog(null, "Please select a row !");
                return;
            }
            
            FarmerSubsidyWorkRequest request = (FarmerSubsidyWorkRequest)tblFarmerSubsidyRequest.getValueAt(selectedRow, 0);
            if(request.getStatus().equalsIgnoreCase("Subsidy provided")){
                JOptionPane.showMessageDialog(null, "Already provided subsidy"); 
                return;
            }
            
            if(request.getStatus().equalsIgnoreCase("Subsidy Rejected")){
                JOptionPane.showMessageDialog(null, "Already rejected subsidy"); 
                return;
            }
            else{
                
                if(request.getStatus().equalsIgnoreCase("sent") && request.getReceiver()==null){
                    JOptionPane.showMessageDialog(null,"Assign the request first to view it");
                    return;
                } 
                
                request.setStatus("Completed");
                System.out.println("Loss "+request.getRequestedSubsidyValue());
                ProcessSubsidyJPanel processSubsidyJPanel = new ProcessSubsidyJPanel(userProcessContainer, request);
                userProcessContainer.add("processSubsidyJPanel", processSubsidyJPanel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
           
        
       
    }//GEN-LAST:event_btnViewRequestActionPerformed

