    private void btnUpdateRelevanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateRelevanceActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAnswer.getSelectedRow();
        
        DefaultTableModel model = (DefaultTableModel) tblAnswer.getModel();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select the Answer", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            Answer answer = (Answer) tblAnswer.getValueAt(selectedRow, 0);
            if(question.getFarmerAccount().getUsername()==userAccount.getUsername()){
                String combovalue = comboUpdate.getSelectedItem().toString();
            
            if(combovalue=="Yes"){
                model.setValueAt("Useful", selectedRow, 2);
                answer.setIsRelevant(true);
            }
            else if(combovalue=="No"){
                model.setValueAt("", selectedRow, 2);
                answer.setIsRelevant(false);
            }
            
            }else {
                JOptionPane.showMessageDialog(null, "You can update for the questions which you have posted", "Warning", JOptionPane.WARNING_MESSAGE);
            }
            
            
            
        }
        
    }//GEN-LAST:event_btnUpdateRelevanceActionPerformed


